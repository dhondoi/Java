package oprBanding;

public class OprtBanding {

    public static void main(String[] args) {
        // operasi perbandingan
        int a = 100, b = 100;
        System.out.println("Operasi Perbandingan");
        System.out.println(a > b); // lebih besar dari
        System.out.println(a < b); // lebih kecil dari
        System.out.println(a >= b); // lebih besar sama dengan
        System.out.println(a <= b); // lebih kecil sama dengan
        System.out.println(a == b); // sama dengan
        System.out.println(a != b); // tidak sama dengan
    }
}
